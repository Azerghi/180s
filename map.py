def generate_random_map():
    grid = [[1 for _ in range(COLS)] for _ in range(ROWS)]
    print(grid)
    rooms = []

    def carve(x, y):
        if 0 < y < ROWS-1 and 0 < x < COLS-1:
            print("carved at", x, y)
            grid[y][x] = 0

    # Étape 1 — Point d'entrée
    start_x, start_y = 1, ROWS - 2
    carve(start_x, start_y)
    current_x, current_y = start_x, start_y

    # Étape 2 — Random Walk jusqu'au boss
    end_x, end_y = COLS - 3, 1
    steps = 200
    for _ in range(steps):
        direction = random.choice([(1,0), (-1,0), (0,1), (0,-1)])
        nx = current_x + direction[0]
        ny = current_y + direction[1]

        if 1 < nx < COLS - 2 and 1 < ny < ROWS - 2:
            current_x, current_y = nx, ny
            carve(current_x, current_y)
            # élargir le chemin un peu
            carve(current_x+1, current_y)
            carve(current_x, current_y+1)

    # Étape 3 — Ajouter quelques salles
    for _ in range(5):
        w = random.randint(4, 6)
        h = random.randint(3, 5)
        x = random.randint(1, COLS - w - 1)
        y = random.randint(1, ROWS - h - 1)

        # éviter la zone du boss
        if x > COLS - 12 and y < 8:
            continue

        for i in range(h):
            for j in range(w):
                carve(x + j, y + i)
        rooms.append((x + w // 2, y + h // 2))

    # Étape 4 — Salle du boss
    def create_room(top, left, height, width):
        for row in range(top, top + height):
            for col in range(left, left + width):
                if 0 < row < ROWS-1 and 0 < col < COLS-1:
                    grid[row][col] = 0

    create_room(1, COLS - 9, 6, 8)

    return grid, (start_y, start_x), (4, COLS - 5), rooms
